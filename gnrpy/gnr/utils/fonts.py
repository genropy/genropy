# Oblique variants share advance widths with their upright counterparts (slant only)
_AFM_WIDTHS['Helvetica-Oblique'] = _AFM_WIDTHS['Helvetica']
_AFM_WIDTHS['Helvetica-BoldOblique'] = _AFM_WIDTHS['Helvetica-Bold']
# Courier variants are all monospaced at 600 units
_AFM_WIDTHS['Courier-Bold'] = _AFM_WIDTHS['Courier']
_AFM_WIDTHS['Courier-Oblique'] = _AFM_WIDTHS['Courier']
_AFM_WIDTHS['Courier-BoldOblique'] = _AFM_WIDTHS['Courier']

_DEFAULT_CHAR_WIDTH = 556  # fallback for unknown chars (avg lowercase Helvetica)


def string_width(text, font_name='Helvetica', font_size=10):
    """Return the width in points of *text* rendered in *font_name* at *font_size* pt.

    Uses embedded AFM metrics — no font files or external dependencies needed.
    Falls back to Helvetica widths for unknown font names.
    """
    widths = _AFM_WIDTHS.get(font_name) or _AFM_WIDTHS['Helvetica']
    return sum(widths.get(c, _DEFAULT_CHAR_WIDTH) for c in (text or '')) * font_size / 1000.0
